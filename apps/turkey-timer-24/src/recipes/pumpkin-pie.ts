/*
Ingredients

APF, 1.5 C.
Cornmel, fine, 1/2 C.
Salt, 1 t.
Apple Cider vinegar, 2 t.
Butter, unsalted, 12 T.
White sugar, 1 T. (heaping)
Ice Water, 1/4 -1/3 C.

Procedure
measure out all dry ingredients into large bowl. mix together until well incorporated.

cut cold butter into vague tablespoons and add to your bowl. 
toss with hands until butter is coated in flour mixture.

smash, squeeze, and break cold butter chunks with your fingers until 
some of the mixture resembles a coarse meal but there are still plenty of 
pieces of butter about the size of blueberries in the mix. 

if butter has gone soft, place entire bowl into fridge for 15 minutes to firm up. 
otherwise, proceed.

mix vinegar and ice water together, then add to flour mixture one tablespoon at 
a time, tossing with hands in between additions. 

the amount of water you will needs varies —  so add water until mixture holds 
together when pressed into a ball, but is still a little crumbly, and isn't sticky to the touch. 

form into a disc, wrap tightly in plastic wrap and refrigerate for at least 30 minutes.

once firmed up, proceed. if your dough is too hard to roll out, allow it 
to sit at room temp for 5 minutes. 

roll out dough into a long rectangle-ish shape — short side facing you. 
the sides of your dough may have cracked a little, that is totally fine at this step.

fold the dough like a letter: fold the far side down towards you, then the 
side closest to you on top of that.  

press it all back together, rotate the dough 90 degrees, then repeat the roll and fold.

form into a disc. return to fridge for another 20 - 30 minutes, if the butter has softened too much.

when ready, roll out dough into a circle that is at least two inches 
wider than the base of your pie tin.

carefully transfer your rolled out dough to your tin, draping, 
rather than stretching it into place.

fold excess dough underneath itself to create a lip, and crimp as desired.

return to fridge to firm up, again. 

preheat oven to 350F. 

now, to blind bake. place tin on baking sheet and line inside of pie with foil or parchment. 
fill with your chosen weights. I like to tent the edges of my crust with foil to prevent burning.

place in oven and allow to bake for about 30-40 minutes, checking occasionally to 
ensure the edges of your crust have not taken on too much color. 

when the edges of your pie are lightly browned, remove weights from inside of pie to properly 
cook the center — prick with a fork to prevent ballooning.

continue to cook for an additional 15 - 20 minutes, until your pie is golden brown in color. 

cool for at least 1 hour before filling. best used day of.
*/

